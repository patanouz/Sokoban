package sokoban;

public class position {

	
	private int positionX;
	private int positionY;
	
	
	public position(int positionX, int positionY) {
		
		this.positionX = positionX;
		this.positionY = positionY;
		
	}
	
	public int getX() {
		return positionX;
	}
	
	public int getY() {
		return positionY;
	}
	
	
	
	
	
}
